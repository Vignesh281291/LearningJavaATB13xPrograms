package ex_05_Operators;

public class Lab022_ArithmeticOperators {
    public static void main(String[] args) {
        int a =10;
        int b=5;
        float c=5.5f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);
        //% Modulus returns Remainder

    }
}
