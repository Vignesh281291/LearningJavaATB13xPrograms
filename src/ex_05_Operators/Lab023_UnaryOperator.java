package ex_05_Operators;

public class Lab023_UnaryOperator {
    public static void main(String[] args) {
        //Unary operator
        int a = +20;
        int b = -10;
        int c = a+b;
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        int d = 1;
        d = d+1;
        System.out.println(d);
    }
}
