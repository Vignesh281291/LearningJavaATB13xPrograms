package ex_09_Increment_Decrement_Operator;

public class Lab069_Increment_Decrement {
    public static void main(String[] args) {
        int a = 10;
        int b = ++a;
        System.out.println("a:" +a);
        System.out.println("b:" +b);
        //Expression & Result Table
        //Line No  |  a  |  Result b
        //   5     |  10 |    -
        //   6     |  11 |    11
    }
}
