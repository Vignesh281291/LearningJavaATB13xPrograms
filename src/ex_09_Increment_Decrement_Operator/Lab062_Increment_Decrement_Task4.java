package ex_09_Increment_Decrement_Operator;

public class Lab062_Increment_Decrement_Task4 {
    public static void main(String[] args) {
        int x = 5;
        System.out.println(++x); // x will be 6
        System.out.println(x++); // x will be 6
        System.out.println(x); // x will be 7
        //Expression & Result Table
        //Line No  |  x  |  Result
        //   5     |  5  |    -
        //   6     |  5 |    6
        //   7     |  6 |    6
        //   8     |  7 |    7
    }
}
