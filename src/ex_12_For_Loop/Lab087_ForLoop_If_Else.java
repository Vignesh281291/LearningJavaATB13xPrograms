package ex_12_For_Loop;

public class Lab087_ForLoop_If_Else {
    public static void main(String[] args) {
        for(int i=0;i<=20;i++)
        {
            if(i>15)
            {
                System.out.println("Loop reaches the value where i is now greater than 15");
            }
            else
            {
                System.out.println("If condition not satisfied");
            }
        }
    }
}
