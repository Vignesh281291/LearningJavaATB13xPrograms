package ex_12_For_Loop;

public class Lab092_ForLoop_Logic {
    public static void main(String[] args) {
        for(int i=0;i<=10;i++) // 0 to 10 --- Runs 11 times
        {
            System.out.println(i);
        }
        for(int j=0;j<10;j++) // 0 to 9 --- Runs 10 times
        {
            System.out.println(j);
        }
        for(int k=1;k<=10;k++) // 1 to 10 --- Runs 10 times
        {
            System.out.println(k);
        }
    }
}
