package ex_12_For_Loop;

public class Lab083_ForLoop {
    public static void main(String[] args) {
        //System.out.println(1);
        //System.out.println(2);
        //System.out.println(3);
        //System.out.println(4);
        //System.out.println(5);
        //For Loop repeat the block of code
        // I - Initialization; C - Condition; U - Update (increment/decrement)
        for(int a=0;a<10;a++) // 0 to 9 --- Runs 10 times
        {
            System.out.println(a);
        }
        for(int b=1;b<10;b++) // 1 to 10 --- Runs 9 times
        {
            System.out.println(b);
        }
    }
}
