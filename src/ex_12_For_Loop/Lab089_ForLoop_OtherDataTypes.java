package ex_12_For_Loop;

public class Lab089_ForLoop_OtherDataTypes {
    public static void main(String[] args) {
        for(float f=1.2f;f<2.34;f++)
        {
            System.out.println(f);
        }
        for(long l=100l;l<110;l++)
        {
            System.out.println(l);
        }
    }
}
