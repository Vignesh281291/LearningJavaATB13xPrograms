package ex_11_Swicth;

public class Lab082_Switch_InterviewQ {
    public static void main(String[] args) {
        char c = 'C';
        switch (c)
        {
            case 'A':
                System.out.println("65");
                break;
            case 'B':
                System.out.println("66");
                break;
            default:
                System.out.println("Print C");
        }
    }
}
